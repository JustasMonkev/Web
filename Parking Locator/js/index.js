function searchParking(){
  var foundParking = [];
  var zipCode = document.getElementById('zip-code-input').value;
  if(zipCode){
      for(var store of stores){
          var postal = store['address']['postalCode'].substring(0, 5);
          if(postal == zipCode){
              foundParking.push(store);
          }
      }
  }
  clearLocations();
  displayParking(foundParking);
  showParkingMarkers(foundParking);
  setOnClickListener();
}
function clearLocations(){
  infoWindow.close();
  for (var i = 0; i < markers.length; i++) {
    markers[i].setMap(null);
  }
  markers.length = 0;
}
function setOnClickListener(){
  var storeElements = document.querySelectorAll('.store-container');
  storeElements.forEach(function(elem, index){
      elem.addEventListener('click', function(){
          new google.maps.event.trigger(markers[index], 'click');
      })
  })
}
function displayParking(){
    var storesHtml = '';
    for(var [index, store] of stores.entries()){
        var address = store['addressLines'];
        var phone = store['phoneNumber'];
        storesHtml += `
            <div class="store-container">
                <div class="store-info-container">
                    <div class="store-address">
                        <span>${address[0]}</span>
                        <span>${address[1]}</span>
                    </div>
                    <div class="store-phone-number">${phone}</div>
                </div>
                <div class="store-number-container">
                    <div class="store-number">
                        ${index+1}
                    </div>
                </div>
            </div>
        `
        document.querySelector('.stores-list').innerHTML = storesHtml;
    }
}


function showParkingMarkers(){
    var bounds = new google.maps.LatLngBounds();
    for(var [index, store] of stores.entries()){
        var latlng = new google.maps.LatLng(
            store["coordinates"]["latitude"],
            store["coordinates"]["longitude"]
            
            );
        var name = store["name"];
        var address = store["addressLines"][0];
        var phone = store["phoneNumber"];
        bounds.extend(latlng);
       createMarker(latlng, name, address,phone)
    }
    map.fitBounds(bounds);
}

function createMarker(latlng, name, address,phone){
   var image = "https://findicons.com/files/icons/951/google_maps/32/parking.png";
   var html = 
    `<div class=info-box>
    <b>${name}</b>
    <br/>
    <br/>
    <i class="fas fa-thumbtack"></i> <a href="https://maps.google.com/?q=${name}" target="_blank">${address}</a><br>
    <i class="fas fa-phone"></i> <span class="phone">${phone}</span>
    </div>`;
    var marker = new google.maps.Marker({
      map: map,
      icon: image,
      position: latlng,
    });
    google.maps.event.addListener(marker, 'click', function() {
      infoWindow.setContent(html);
      infoWindow.open(map, marker);
    });
    markers.push(marker);
}
